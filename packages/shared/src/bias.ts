// ── ICC Market Bias Engine Types ──────────────────────────────────
//
// ICC = Indication, Correction, Continuation — a price-action methodology
// that reads pure swing structure on a "boss" timeframe (4H) to determine
// higher-timeframe directional bias. These types are shared between the
// api-gateway worker (producer) and the web frontend (consumer).

export type BiasDirection = 'BULLISH' | 'BEARISH' | 'NEUTRAL';

export type MarketStateKind = 'UPTREND' | 'DOWNTREND' | 'CONSOLIDATION';

export type ICCPhaseKind =
  | 'INDICATION'
  | 'CORRECTION'
  | 'CONTINUATION'
  | 'NO_SETUP';

export type CorrectionZoneKind =
  | 'Above Impulse'
  | 'Shallow (0-38%)'
  | 'Optimal (38-62%)'
  | 'Deep (62-79%)'
  | 'Critical (79%+)'
  | 'N/A';

export type SessionKind =
  | 'Asian'
  | 'London'
  | 'New York'
  | 'London-NY Overlap'
  | 'Off-Hours';

export type MomentumKind = 'Strong' | 'Moderate' | 'Weak' | 'Indecisive';

export interface SwingPoint {
  type: 'high' | 'low';
  price: number;
  /** Index into the candle array passed to the analyzer (0 = oldest). */
  index: number;
}

export interface MarketStateModule {
  score: number;
  state: MarketStateKind;
  swingSequence: string;
  tradeable: boolean;
}

export interface ICCPhaseModule {
  score: number;
  current: ICCPhaseKind;
  detail: string;
  indicationLevel: number | null;
  correctionDepth: number | null;
  entryReady: boolean;
}

export interface SwingStructureModule {
  score: number;
  keyLevels: {
    lastSwingHigh: number | null;
    lastSwingLow: number | null;
    previousSwingHigh: number | null;
    previousSwingLow: number | null;
  };
  recentBreaks: Array<{ level: number; direction: 'above' | 'below'; barsAgo: number }>;
}

export interface CorrectionModule {
  score: number;
  impulseRange: { high: number; low: number } | null;
  currentDepth: number | null;
  inOptimalZone: boolean;
  zone: CorrectionZoneKind;
  retracementLevels: {
    r25: number;
    r38: number;
    r50: number;
    r62: number;
    r79: number;
  } | null;
}

export interface SessionModule {
  score: number;
  active: SessionKind;
  momentum: MomentumKind;
  relevance: 'High' | 'Medium' | 'Low';
  recentCandleProfile: string;
}

export interface ICCBreakdown {
  marketState: MarketStateModule;
  phase: ICCPhaseModule;
  structure: SwingStructureModule;
  correction: CorrectionModule;
  session: SessionModule;
}

/** Suggested reference levels derived from ICC structure. Only present
 *  when phase === 'CONTINUATION' AND tradeable AND the impulse range is
 *  clean. Always framed as planning reference — real entries require a
 *  lower-TF trigger per ICC doctrine. */
export interface TradePlan {
  direction: 'long' | 'short';
  entry: number;
  stopLoss: number;
  takeProfit1: number;
  takeProfit2: number;
  slDistancePct: number;
  tp1DistancePct: number;
  tp2DistancePct: number;
  rationaleSl: string;
}

export interface AssetBias {
  symbol: string;
  label: string;
  category: 'Metal' | 'Index' | 'Forex';
  price: number;
  change24h: number;
  bias: BiasDirection;
  score: number;
  confidence: number;
  tradeable: boolean;
  icc: ICCBreakdown;
  sparkData: number[];

  /** Optional 1H timeframe analysis. When present and both tradeable in the
   *  same direction, `confluence.aligned` is true — the A+ setup signal. */
  icc1H?: ICCBreakdown;
  bias1H?: BiasDirection;
  score1H?: number;
  confluence?: {
    aligned: boolean;
    direction: BiasDirection;
    reason: string;
  };

  /** One-sentence AI narrative generated by the cron. Empty if not yet
   *  generated or if the AI call fell back to the deterministic template. */
  narrative?: string;

  /** 4H-timeframe reference trade plan (entry/SL/TP). Present only when
   *  the 4H is in CONTINUATION + tradeable. */
  tradePlan?: TradePlan;

  /** 1H-timeframe reference trade plan. Present only when 1H is in
   *  CONTINUATION + tradeable. */
  tradePlan1H?: TradePlan;

  /** Present when the asset could not be analyzed this cycle. */
  error?: string;
}

