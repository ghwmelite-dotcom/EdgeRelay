export interface AlertHistoryItem {
  id: string;
  firedAtUnix: number;
  symbol: string;
  interval: '4h' | '1h' | '15min';
  phase: string;      // ICC phase kind OR ORB session+direction label
  previousPhase: string | null;
  bias: BiasDirection;
  quality: AlertQuality;
  metCount: number;
  totalCount: number;
  criteria: AlertCriterion[];
  tradePlan: TradePlan | null;
  narrative: string | null;
  readAtUnix: number | null;
  /** 'ICC' (bias transition) or 'ORB' (opening-range breakout). Drives
   *  the strategy badge and color scheme in the inbox UI. */
  strategy: 'ICC' | 'ORB';
}

export interface InboxResponse {
  unreadCount: number;
  alerts: AlertHistoryItem[];
}

/** OHLC point for the live chart feed. `time` is unix seconds. */
export interface ChartCandle {
  time: number;
  open: number;
  high: number;
  low: number;
  close: number;
}

/** Swing marker drawn on the live chart. */
export interface ChartSwing {
  time: number;
  type: 'high' | 'low';
  price: number;
}

/** Full payload returned by /v1/bias/candles/:symbol/:interval. */
export interface BiasCandlesPayload {
  symbol: string;
  interval: '4h' | '1h';
  candles: ChartCandle[];
  state: {
    bias: BiasDirection;
    score: number;
    phase: ICCPhaseKind;
    marketState: MarketStateKind;
    correctionDepth: number | null;
  };
  swings: ChartSwing[];
  indicationLevel: number | null;
  impulseRange: { high: number; low: number } | null;
  retracementLevels: {
    r25: number;
    r38: number;
    r50: number;
    r62: number;
    r79: number;
  } | null;
  tradePlan: TradePlan | null;
}

