// ── Alert quality tiers ───────────────────────────────────────
//
// Every phase-transition alert the engine fires is graded against 8
// criteria. The grade tells users at a glance whether a signal had all
// stars aligned (A+) or just partial confirmation (B/C), so they can
// size position / stand aside accordingly. Pure function — same logic
// runs in the cron (on persist) and in the UI (on render).

export type AlertQuality = 'A_PLUS' | 'A' | 'B' | 'C';

export interface AlertCriterion {
  key: string;
  label: string;
  met: boolean;
}

export interface AlertQualityResult {
  quality: AlertQuality;
  metCount: number;
  totalCount: number;
  criteria: AlertCriterion[];
  headlineWarning: string | null;   // short reason set when not A+
}

export function computeAlertQuality(
  asset: Pick<AssetBias, 'bias' | 'score' | 'confidence' | 'tradeable' | 'icc' | 'bias1H' | 'score1H' | 'icc1H' | 'confluence'>,
  interval: '4h' | '1h',
): AlertQualityResult {
  const icc = interval === '4h' ? asset.icc : (asset.icc1H ?? asset.icc);
  const phase = icc.phase.current;
  const otherBias = interval === '4h' ? asset.bias1H : asset.bias;

  const criteria: AlertCriterion[] = [
    {
      key: 'phase_continuation',
      label: 'Phase is Continuation (entry-ready)',
      met: phase === 'CONTINUATION',
    },
    {
      key: 'tradeable',
      label: 'Tradeable (non-NEUTRAL, clean trend)',
      met: asset.tradeable,
    },
    {
      key: 'other_tf_agree',
      label: `${interval === '4h' ? '1H' : '4H'} agrees on direction`,
      met: otherBias !== undefined && otherBias !== 'NEUTRAL' && otherBias === asset.bias,
    },
    {
      key: 'confluence_aligned',
      label: '4H + 1H both in Continuation (A+ confluence)',
      met: !!asset.confluence?.aligned,
    },
    {
      key: 'optimal_zone',
      label: 'Correction in optimal 38–62% zone',
      met: icc.correction.inOptimalZone === true,
    },
    {
      key: 'session_relevance',
      label: 'Active session suits this asset',
      met: icc.session.relevance === 'High' || icc.session.relevance === 'Medium',
    },
    {
      key: 'momentum',
      label: 'Recent candles show real momentum',
      met: icc.session.momentum === 'Strong' || icc.session.momentum === 'Moderate',
    },
    {
      key: 'confidence',
      label: 'Confidence ≥ 60%',
      met: asset.confidence >= 60,
    },
  ];

  const metCount = criteria.filter((c) => c.met).length;
  const totalCount = criteria.length;

  // Tier rules:
  //   A+ requires the three strongest signals: confluence + optimal zone
  //      + either high-relevance session or strong momentum
  //   A  = 6+ criteria met
  //   B  = 4-5 criteria met
  //   C  = <= 3 criteria met
  const hasConfluence = criteria[3]!.met;
  const hasOptimal    = criteria[4]!.met;
  const hasSessionContext = criteria[5]!.met || criteria[6]!.met;

  let quality: AlertQuality;
  if (hasConfluence && hasOptimal && hasSessionContext) quality = 'A_PLUS';
  else if (metCount >= 6) quality = 'A';
  else if (metCount >= 4) quality = 'B';
  else quality = 'C';

  // Headline warning for the UI chip
  let headlineWarning: string | null = null;
  if (quality !== 'A_PLUS') {
    const missed: string[] = [];
    if (!hasConfluence) missed.push('1H not confirming');
    if (!hasOptimal)    missed.push('not in optimal 38–62% zone');
    if (!hasSessionContext) missed.push('off-hours for this asset');
    if (phase !== 'CONTINUATION') missed.push('not yet Continuation');
    if (missed.length > 0) headlineWarning = missed.slice(0, 2).join(' · ');
  }

  return { quality, metCount, totalCount, criteria, headlineWarning };
}

